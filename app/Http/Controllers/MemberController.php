    {
        $request->validate([
            'name' => ['required'],
            'email' => ['required', 'email', 'regex:/^(?!:\/\/)(?=.{1,255}$)((.{1,63}\.){1,127}(?![0-9]*$)[a-z0-9-]+\.?)$/i'],
            'telp' => ['required', 'numeric', 'regex:/^(\+62)[1-9]{7,14}$/']
        ]);
        
